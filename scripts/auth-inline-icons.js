/**
 * Sustituye cada <i data-lucide="…"> por el SVG equivalente si existe en ICON.
 */
export function hydrateAuthIconPlaceholders(root = document) {
  root.querySelectorAll("i[data-lucide]").forEach((el) => {
    const key = el.getAttribute("data-lucide");
    if (!key) return;
    const html = ICON[key];
    if (!html) return;
    const tpl = document.createElement("template");
    tpl.innerHTML = html.trim();
    const node = tpl.content.firstElementChild;
    if (node) el.replaceWith(node);
  });
}
